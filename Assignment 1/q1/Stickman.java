package q1;

/**
 * <p>
 * This program will generate a stick man by using the print line method.
 * </p>
 *
 * @author Zhang,Yongjian,SetA
 * @version 1.0
 */
public class Stickman {
    /**
     * <p>
     * This is the main method (entry point) that gets called by the JVM.
     * </p>
     *
     * @param args
     *            command line arguments.
     */
    public static void main(String[] args) {

        /*
         * by using tab, backslash and other characters, we can print a stick
         * man with print line method
         */
        System.out.println("\t \t \t \t \t /\"\"\"\"\"\"\\");
        System.out.println("\t \t \t \t \t| (o||o) |");
        System.out.println("\t \t \t \t \t| '____' |");
        System.out.println("\t \t \t \t \t \\______/");
        System.out.println("\t \t \t \t \t ___||___");
        System.out.println("\t \t \t \t \t/        \\");
        System.out.println("                                       " 
        + "|  |    |  |");
        System.out.println("\t \t \t \t \t\\_|____|_/");
        System.out.println("\t \t \t \t \t/_|    |_\\");
        System.out.println("\t \t \t \t \t|  .--.  |");
        System.out.println("\t \t \t \t \t\\._|  |_./");
        System.out.println("\t \t \t \t \t/__|  |__\\");

        System.out.println("Question one was called and ran sucessfully.");

    }

};
